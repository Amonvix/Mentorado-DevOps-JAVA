package fundamentos;

public class PrimeiroPrograma {
	public static void main(String[] args) {
		
		System.out.println("Hello World!"); 		//Comando usando para se livrar da maldição da programação
		System.out.println("Primeiro programa");	//Comando print para exibir dados na tela
		System.out.print("Terceira linha \n");		//Comando usado para exibir dados na tela sem a mudança para a linha seguinte
		System.out.printf("Quarta linha formatada");//Comando usado para exibir dados na tela formatados e sem mudança de linha
	}

}
